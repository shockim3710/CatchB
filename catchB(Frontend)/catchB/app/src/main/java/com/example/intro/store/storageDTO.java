package com.example.intro.store;

public class storageDTO {
    private String gift_credit;
    private String gift_title;
    private String gift_pic;

    public String getGift_credit() {
        return gift_credit;
    }

    public void setGift_credit(String gift_credit) {
        this.gift_credit = gift_credit;
    }

    public String getGift_title() {
        return gift_title;
    }

    public void setGift_title(String gift_title) { this.gift_title = gift_title; }

    public String getGift_pic() {
        return gift_pic;
    }

    public void setGift_pic(String gift_pic) {
        this.gift_pic = gift_pic;
    }
}
